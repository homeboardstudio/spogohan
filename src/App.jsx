function calcNeeds(ageId, sportId) {
  const age   = AGES.find(a => a.id === ageId);
  const sport = SPORTS.find(s => s.id === sportId);
  if (!age || !sport) return { cal: 2000, protein: 60 };
  return { cal: Math.round(age.baseCal * sport.cal), protein: Math.round(age.baseProtein * sport.cal) };
}

function getDislikedIngredients(ids) { return ids.flatMap(id => DISLIKE_MAP[id] || []); }

function filterByDislikes(meals, dislikedNames) {
  if (!dislikedNames.length) return meals;
  return meals.filter(m => !m.items.some(it => dislikedNames.some(d => it.n.includes(d))));
}

function pickMeal(meals, dislikedNames, usedNames = []) {
  const filtered = filterByDislikes(meals, dislikedNames).filter(m => !usedNames.includes(m.name));
  if (!filtered.length) {
    const fb = filterByDislikes(meals, dislikedNames);
    return fb.length ? fb[0 | Math.random() * fb.length] : meals[0 | Math.random() * meals.length];
  }
  return filtered[0 | Math.random() * filtered.length];
}

function generateDailyPlan(goalId, dislikedNames, usedNames = [], targetCal = null) {
  const db = MEAL_DB[goalId] || MEAL_DB.stamina;
  const b = pickMeal(db.breakfast, dislikedNames, usedNames);
  const l = pickMeal(db.lunch, dislikedNames, [...usedNames, b.name]);
  const d = pickMeal(db.dinner, dislikedNames, [...usedNames, b.name, l.name]);
  const s = pickMeal(db.snack, dislikedNames, [...usedNames, b.name, l.name, d.name]);

  // ご飯量自動調整: 目標カロリーに近づくよう主食量をスケーリング
  if (targetCal) {
    const plan = { breakfast: b, lunch: l, dinner: d, snack: s };
    return adjustRiceForTarget(plan, targetCal);
  }
  return { breakfast: b, lunch: l, dinner: d, snack: s };
}

/** ご飯量自動調整 — 目標カロリーとの差分をご飯(主食)の増減で吸収
 *  ご飯100gあたり: 168kcal, P2.5g, C37.1g, F0.3g */
const RICE_PER_100G = { cal: 168, p: 2.5, c: 37.1, f: 0.3 };
const RICE_NAMES = ["ご飯","ごはん"];

function adjustRiceForTarget(plan, targetCal) {
  // 現在の合計カロリー
  const currentCal = ["breakfast","lunch","dinner","snack"].reduce((sum, k) => sum + (plan[k]?.cal || 0), 0);
  const diff = targetCal - currentCal;

  // 差が小さければ（±100kcal以内）調整不要
  if (Math.abs(diff) < 100) {
    const pct = Math.round(currentCal / targetCal * 100);
    return { ...plan, _riceAdjust: { adjusted: false, pct, targetCal, details: [] } };
  }

  // 朝・昼・夕の3食からご飯を持つ食事を特定
  const mealKeys = ["breakfast","lunch","dinner"];
  const MEAL_JP = { breakfast:"朝", lunch:"昼", dinner:"夕" };
  const riceInfo = mealKeys.map(k => {
    const meal = plan[k];
    if (!meal) return null;
    const riceItem = meal.items.find(it => RICE_NAMES.some(rn => it.n.includes(rn)));
    if (!riceItem) return null;
    return { key: k, item: riceItem, currentG: riceItem.q };
  }).filter(Boolean);

  if (!riceInfo.length) {
    const pct = Math.round(currentCal / targetCal * 100);
    return { ...plan, _riceAdjust: { adjusted: false, pct, targetCal, details: [] } };
  }

  // 差分をご飯のグラム数に変換し、ご飯のある食事に均等配分
  const extraGPerMeal = Math.round(diff / RICE_PER_100G.cal * 100 / riceInfo.length);

  const adjusted = { ...plan };
  const details = [];
  riceInfo.forEach(ri => {
    const meal = { ...adjusted[ri.key] };
    const newG = Math.min(400, Math.max(100, ri.currentG + extraGPerMeal)); // 最低100g、最大400g
    const deltaG = newG - ri.currentG;
    const ratio = deltaG / 100;

    details.push({ meal: MEAL_JP[ri.key], beforeG: ri.currentG, afterG: newG });

    // 栄養値を更新
    meal.cal = Math.round(meal.cal + RICE_PER_100G.cal * ratio);
    meal.p   = Math.round(meal.p   + RICE_PER_100G.p * ratio);
    meal.c   = Math.round(meal.c   + RICE_PER_100G.c * ratio);
    meal.f   = Math.round(meal.f   + RICE_PER_100G.f * ratio);

    // 材料リストのご飯量を更新
    meal.items = meal.items.map(it =>
      RICE_NAMES.some(rn => it.n.includes(rn)) ? { ...it, q: newG } : it
    );
    adjusted[ri.key] = meal;
  });

  // 調整後の合計カロリーで充足率を再計算
  const adjustedCal = ["breakfast","lunch","dinner","snack"].reduce((sum, k) => sum + (adjusted[k]?.cal || 0), 0);
  const pct = Math.round(adjustedCal / targetCal * 100);

  adjusted._riceAdjust = { adjusted: true, pct, targetCal, details };
  return adjusted;
}

function getRecipe(name) {
  const r = RECIPE_DB[name];
  if (!r) return null;
  return { steps: r[0].split("|"), tips: r[1], makeAhead: r[2] };
}

function sumNutrition(plan, skipKeys = []) {
  let cal = 0, p = 0, c = 0, f = 0;
  ["breakfast","lunch","dinner","snack"].forEach(k => {
    if (!plan[k] || skipKeys.includes(k)) return;
    cal += plan[k].cal; p += plan[k].p; c += plan[k].c; f += plan[k].f;
    if (plan[k].extra) { cal += plan[k].extra.cal; p += plan[k].extra.p; c += plan[k].extra.c; f += plan[k].extra.f; }
  });
  return { cal, p, c, f };
}

